package com.peprally.jeremy.peprally.data;

public class PlayerProfile {
	private String team;
	private String firstName;
	private String lastName;
	private String imageURL;
	private String year;
	private String height;
	private String weight;
	private String position;
	private String hometown;
	private String gender;
	private String username;
	private int index;
	private int number;
	private boolean hasUserProfile;
	
	public String getTeam() {
		return team;
	}
	
	public String getFirstName() {
		return firstName;
	}
	
	public String getLastName() {
		return lastName;
	}
	
	public String getImageURL() {
		return imageURL;
	}
	
	public String getYear() {
		return year;
	}
	
	public String getHeight() {
		return height;
	}
	
	public String getWeight() {
		return weight;
	}
	
	public String getPosition() {
		return position;
	}
	
	public String getHometown() {
		return hometown;
	}
	
	public String getGender() {
		return gender;
	}
	
	public String getUsername() {
		return username;
	}
	
	public int getIndex() {
		return index;
	}
	
	public int getNumber() {
		return number;
	}
	
	public boolean isHasUserProfile() {
		return hasUserProfile;
	}
	
	public void setTeam(String team) {
		this.team = team;
	}
	
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	
	public void setImageURL(String imageURL) {
		this.imageURL = imageURL;
	}
	
	public void setYear(String year) {
		this.year = year;
	}
	
	public void setHeight(String height) {
		this.height = height;
	}
	
	public void setWeight(String weight) {
		this.weight = weight;
	}
	
	public void setPosition(String position) {
		this.position = position;
	}
	
	public void setHometown(String hometown) {
		this.hometown = hometown;
	}
	
	public void setGender(String gender) {
		this.gender = gender;
	}
	
	public void setUsername(String username) {
		this.username = username;
	}
	
	public void setIndex(int index) {
		this.index = index;
	}
	
	public void setNumber(int number) {
		this.number = number;
	}
	
	public void setHasUserProfile(boolean hasUserProfile) {
		this.hasUserProfile = hasUserProfile;
	}
}
